function parseCSV(csv) {
  const lines = csv.trim().split(/\r?\n/);
  const out = [];
  for (let i = 1; i < lines.length; i++) {
    const line = lines[i];
    if (!line) continue;
    const fields = [];
    let cur = ""; let inQ = false;
    for (let j = 0; j < line.length; j++) {
      const ch = line[j];
      if (ch === '"') { inQ = !inQ; continue; }
      if (ch === ',' && !inQ) { fields.push(cur.trim()); cur = ""; } else cur += ch;
    }
    fields.push(cur.trim());
    if (fields.length >= 2) {
      out.push({ name: fields[0].replace(/^"|"$/g, ''), address: fields[1].replace(/^"|"$/g, '') });
    }
  }
  return out;
}

function pickCoords(address) {
  for (const [re, lat, lng] of LOCALITY_COORDS) {
    if (re.test(address)) return [lat, lng];
  }
  // Default Tamil Nadu centroid
  return [11.1271, 78.6569];
}

// Deterministic jitter from string (small spread to avoid stacking exact duplicates)
function jitterFromName(name) {
  let hash = 0;
  for (let i = 0; i < name.length; i++) hash = (hash * 31 + name.charCodeAt(i)) >>> 0;
  const delta = ((hash % 1000) / 1000 - 0.5) * 0.02; // Â±0.01 deg
  const delta2 = (((hash / 1000) % 1000) / 1000 - 0.5) * 0.02;
  return [delta, delta2];
}

